package com.choice.wechat.domain.cookie;

public class Cookie {

	private String pk_cookie;
	private String appid;
	private String openid;
	private String cookie;
	
	public String getPk_cookie() {
		return pk_cookie;
	}
	public void setPk_cookie(String pk_cookie) {
		this.pk_cookie = pk_cookie;
	}
	public String getAppid() {
		return appid;
	}
	public void setAppid(String appid) {
		this.appid = appid;
	}
	public String getOpenid() {
		return openid;
	}
	public void setOpenid(String openid) {
		this.openid = openid;
	}
	public String getCookie() {
		return cookie;
	}
	public void setCookie(String cookie) {
		this.cookie = cookie;
	}
}
